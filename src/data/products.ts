export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category);
}

export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByGender(category: string, gender: string): Product[] {
  return products.filter(p => 
    p.category === category && 
    (gender === 'all' || p.gender === gender)
  );
}

export function getBrands(category: string): { name: string; count: number }[] {
  const categoryProducts = getProductsByCategory(category);
  const brandCounts: Record<string, number> = {};
  
  categoryProducts.forEach(product => {
    if (product.brand) {
      brandCounts[product.brand] = (brandCounts[product.brand] || 0) + 1;
    }
  });
  
  return Object.entries(brandCounts)
    .map(([name, count]) => ({ name, count }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

export function getMaxPrice(category: string): number {
  const categoryProducts = getProductsByCategory(category);
  return Math.max(...categoryProducts.map(p => p.price), 2000);
}
